public class Ex3 {
	public static void main(String args[]) {
		
		for (int i = 233; i < 457; i++) {
			if (i > 300 && i < 400) {
				i = i +2;
				System.out.println("Soma de três em três: " +i);
			} else {
				i = i +4;
				System.out.println("Soma de cinco em cinco: " +i);
			}
		} 
			
	}

}
